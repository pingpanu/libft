#include "libft.h"

void    ft_bzero(void *s, size_t n)
{
    size_t  i;
    char    *res;

    i = 0;
    res = (char*)s;
    if (n == 0)
        return ;
    while(i < n)
    {
        res[i] = '\0';
        i++;
    }
}