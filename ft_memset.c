#include "libft.h"

void    *ft_memset(void *b, int c, size_t len)
{
    size_t  i;
    char    *buc;

    i = 0;
    buc = (char *)b;
    while (i < len)
    {
        buc[i] = c;
        i++;
    }
    return (buc);
}