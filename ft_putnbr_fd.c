#include "libft.h"

void    ft_putnbr_fd(int n, int fd)
{
    if (fd < 0)
        return ;
    if (n == -2147483648)
    {
        write(fd, "-21", 3);
        n = 47483648;
    }
    if (n < 0)
    {
        ft_putchar_fd('-', fd);
        n *= -1;
    }
    if (n >= 10)
        ft_putnbr_fd(n / 10, fd);
    ft_putchar_fd(n % 10 + '0', fd);
}